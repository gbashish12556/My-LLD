package model;

public class Location {

    private double xCord;
    private double yCord;

    public Location(double xCord, double yCord) {
        this.xCord = xCord;
        this.yCord = yCord;
    }

    public double getxCord() {
        return xCord;
    }

    public double getyCord() {
        return yCord;
    }

}
